    public void mostrar() {

        //llenado del arreglo con la informacion a mostrar
        usaclibrary.USACLibrary.recorridosAVL.resetearLista(); // limpiar lista
        usaclibrary.USACLibrary.arbolAvl.postOrdenReporte(usaclibrary.USACLibrary.arbolAvl.root); // llenar lista para reporte

        String matriz[][] = new String[usaclibrary.USACLibrary.recorridosAVL.tamano][2]; // matriz para la tabla
        NodoLista aux = usaclibrary.USACLibrary.recorridosAVL.primero; // crecion de auxiliar para acceder a los atrubutos de cada usuario

        int i = 0; // variable que servira para crear filas
        while (aux != null) {

            matriz[i][0] = aux.carnet;// categoria guardada en ese string llamado carnet
            matriz[i][1] = aux.nombre; // nombre del usuario

            i++; //iterador de filas

            aux = aux.siguiente;
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                matriz,
                new String[]{
                    "Categoria", "Creador"
                }
        ));
    }


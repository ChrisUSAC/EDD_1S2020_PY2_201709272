    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int filaSelect = jTable1.getSelectedRow();

        if (filaSelect == -1) {

            JOptionPane.showMessageDialog(this, "Seleccione Cetegoria Primero", "AVISO", 1);

        } else {
            System.out.println("filaSelect " + filaSelect);

            //llamar al metodo para que borre de las categorias si es que es el propietario de la categoria
            String categoria = (String) jTable1.getValueAt(filaSelect, 0);
            System.out.println("categoria: " + categoria);
            String propietario = (String) jTable1.getValueAt(filaSelect, 1);
            System.out.println("propietario: " + propietario);
            
            //si es el propietario el logueado mandar a eliminar de AVL
            if (usaclibrary.USACLibrary.UsuarioLogeado.equals(propietario)) {
                
                
                usaclibrary.USACLibrary.arbolAvl.root =usaclibrary.USACLibrary.arbolAvl.deleteNode(usaclibrary.USACLibrary.arbolAvl.root, categoria);
                JOptionPane.showMessageDialog(this, "Categoria Eliminada Exitosamente", "AVISO", 1);

            } else {
                JOptionPane.showMessageDialog(this, "No puede Eliminar una categoria de otro usuario", "AVISO", 1);
            }

            mostrar();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

